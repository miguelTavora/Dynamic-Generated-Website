function getNameAndDistrict($image,$auth){
    $db = mysqli_connect("localhost", "root", "", "base");

    if (!$db) {
        die("Connection failed: " . mysqli_connect_error());
    }
    $sql = "SELECT * FROM contentfile";

    $result = mysqli_query($db, $sql);
    $output = '';

    if (mysqli_num_rows($result) > 0) {
        while ($row = mysqli_fetch_assoc($result)) {// output data of each row
            if (($row["public"] == 0 || ($row["public"] == 1 && $auth)) && $row["image"] == $image) {
                $output = $output . '<br/>'. $row["name"]. '<br/><br/><br/><strong>District</strong><br/><br/>';
                $output = $output . $row["district"]. '<br/><br/>';
                break;
            }
        }
    } else {
        echo "0 results";
    }
    if($output == ''){
        $output = '<br/>XXXXXXXXXX<br/><br/><br/><strong>District</strong><br/><br/>XXXXXXXXXXXXXXXX<br/><br/>';
    }
    mysqli_close($db);
    return $output;
}

